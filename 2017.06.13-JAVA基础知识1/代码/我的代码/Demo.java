//练习输出不同数据类型的数值
class Demo{
	public static void main(String[] args){
		//输出字符串
		System.out.println("刘凯");
		
		//输出整数
		System.out.println(12);
		
		//输出小数
		System.out.println(12.12);
		
		//输出字符
		System.out.println('c');
		
		//输出布尔类型
		System.out.println(true);
		
		//输出空值
		//System.out.println(null);
	}
}