//练习数据类型间的运算规则
class Demo3{
	public static void main(String[] args){
		//int
		int a = 12;
		System.out.println(a);
		//byte
		byte b = 12;
		System.out.println(b);
		//short
		short c = 12;
		System.out.println(c);
		//long
		long d = 12;
		System.out.println(d);
		//float
		//默认为double类型，需要在数值后面加f
		float f = 12.12f;
		System.out.println(f);
		//double
		double e = 12.14;
		System.out.println(e);
		//boolean
		boolean flag = false;
		System.out.println(flag);
	}
}